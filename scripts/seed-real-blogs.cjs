require('dotenv').config();
const pg = require('pg');

const pool = new pg.Pool({
  connectionString: process.env.DATABASE_URL,
  max: 3,
});

// ---------------------------------------------------------------------------
// Phase 1 helpers
// ---------------------------------------------------------------------------

async function fixExistingBlogs() {
  console.log('=== Phase 1: Fixing existing blogs ===\n');

  // 1-A. Replace "을/를" with the correct particle "를" (most common case
  //       in the generated excerpts: "...을/를 찾고 계신가요?" -> "를 찾고").
  //       We also handle "은/는" just in case.
  const particleRes = await pool.query(`
    UPDATE blogs
    SET content = REPLACE(REPLACE(content, '을/를', '를'), '은/는', '는'),
        excerpt = REPLACE(REPLACE(excerpt, '을/를', '를'), '은/는', '는'),
        title   = REPLACE(REPLACE(title,   '을/를', '를'), '은/는', '는')
    WHERE content LIKE '%을/를%'
       OR content LIKE '%은/는%'
       OR excerpt LIKE '%을/를%'
       OR excerpt LIKE '%은/는%'
       OR title   LIKE '%을/를%'
       OR title   LIKE '%은/는%'
  `);
  console.log(`  Particle fix: ${particleRes.rowCount} rows updated`);

  // 1-B. Normalise categories
  const categoryMap = [
    { from: 'beauty',     to: '뷰티/패션' },
    { from: 'automotive', to: '생활정보'  },
    { from: 'education',  to: '육아/교육' },
    { from: 'cafe',       to: '맛집/식당' },
  ];

  for (const { from, to } of categoryMap) {
    const res = await pool.query(
      `UPDATE blogs SET category = $1 WHERE category = $2`,
      [to, from]
    );
    if (res.rowCount > 0) {
      console.log(`  Category "${from}" -> "${to}": ${res.rowCount} rows`);
    }
  }

  console.log('\nPhase 1 complete.\n');
}

